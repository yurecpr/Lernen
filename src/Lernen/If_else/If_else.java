package Lernen.If_else;

public class If_else {
    public static void main(String[] args) {

        int i = -1;
        if (i > 0) {
            System.out.println("Number is positive");
        } else if (i < 0) {
            System.out.println("Number is negativ");
        } else {
            System.out.println("Number is zero");
        }

    }
}
    