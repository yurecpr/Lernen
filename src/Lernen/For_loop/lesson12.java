package Lernen.For_loop;

public class lesson12 {
    public static void main(String[] args) {


        for (int i = 1, j = 10; i <= j; i++, j--) {
            System.out.println("i:" + i + " ,j:" + j);
        }
        int y = 0;
        for (; y < 5; ) {
            System.out.println(y);
            y++;
        }


    }
}
