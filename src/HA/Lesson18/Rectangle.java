package HA.Lesson18;

public class Rectangle {
    int longed;
    int width;

    public Rectangle(int longed, int width) {
        System.out.println("Long " + longed + " Width " + width + " Area of square= " + (longed * width));
        System.out.println("Long " + longed + " Width " + width + " Perimeter of square= " + ((longed + width) * 2));
    }

}
